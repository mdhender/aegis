#include <common/nstring.h>


nstring
nstring::cat_three(const nstring &str2, const nstring &str3)
    const
{
    string_ty *tmp = str_cat_three(ref, str2.ref, str3.ref);
    nstring result(tmp);
    str_free(tmp);
    return result;
}
